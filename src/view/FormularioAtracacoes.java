    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Atracacoes obj = new Atracacoes();;
        obj.setId_navio(Integer.valueOf(txtCodigoNavio.getText()));
        obj.setDataChegada(txtDataChegada.getText());
        obj.setDataPartida(txtDataPartida.getText());
        obj.setBerco(txtBerco.getText());
        Date agora = new Date(); //Obtendo a data do sistema.
        SimpleDateFormat dataEUA = new SimpleDateFormat("YYYY-MM-dd HH:mm:ss"); //Salvando a data obtida no formato Americano.
        String dataMysql = dataEUA.format(agora); //Mudando o nome para Mysql 
        obj.setDataChegada(dataMysql); //Salvando o valor armazenda no formato acima.
        obj.setDataPartida(dataMysql);
        AtracacoesDAO dao = new AtracacoesDAO();
        dao.Salvar(obj);
        Utilitarios util = new Utilitarios();
        util.LimpaTela(painelDadosPessoais);


    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String Navio = txtCodigoNavio.getText();
        Atracacoes obj = new Atracacoes();
        AtracacoesDAO dao = new AtracacoesDAO();

        obj = dao.BuscarAtracacoes(Navio);
        if (String.valueOf(obj.getId_navio())!= null) {
            txtCodigo.setText(String.valueOf(obj.getId_atracacoes()));
            txtCodigoNavio.setText(String.valueOf(obj.getId_navio()));
            txtDataChegada.setText(obj.getDataChegada());
            txtDataPartida.setText(obj.getDataPartida());
            txtBerco.setText(obj.getBerco());
        } else {
            JOptionPane.showMessageDialog(null, "Registo não encontrado!");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

