        List<Atracacoes> lista = dao.Filtrar(nome);
        DefaultTableModel dados = (DefaultTableModel) tabela.getModel();
        dados.setNumRows(0);
        for (Atracacoes a : lista) {
            dados.addRow(new Object[]{
                a.getId_atracacoes(),
                a.getId_navio(),
                a.getDataChegada(),
                a.getDataPartida(),
                a.getBerco()
            });
        }
